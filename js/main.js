	var animEndEventNames = {
			'WebkitAnimation' : 'webkitAnimationEnd',
			'OAnimation' : 'oAnimationEnd',
			'msAnimation' : 'MSAnimationEnd',
			'animation' : 'animationend'
		},
		// animation end event name
		animEndEventName = animEndEventNames[ Modernizr.prefixed( 'animation' ) ],
		// event type (if mobile use touch events)
		eventtype = mobilecheck() ? 'touchstart' : 'click',
		// support for css animations
		support = Modernizr.cssanimations;

	var $grid = $('#photo-grid'),
				items = $('#photo-grid li'),
				isAnimating = false;

	setTimeout(function() {
		if( isAnimating ) return false;
		isAnimating = true;
		
		// $photos.magicWall("stop");

		loadNewSet( i );
	}, 2000);

	function loadNewSet( set ) {

		// add class "tt-old" to the elements/images that are going to get removed
		$('#photo-grid li .magicwall-wrap').addClass('tt-old');

		// apply effect
		setTimeout( function() {

			// append new elements
			items.each(function(index){
				var template = _.template(
					'<div class="magicwall-wrap">' +
						'<a href="<%- photo.href %>" class="lightbox" target="<%- photo.target %>">' +
							'<div class="magicwall-thumb" style="background-image: url(<%- photo.image_url %>);"></div>' +
						'</a>' +
					'</div>', 
					{ 'photo': ASHLEY_PHOTOS[index] });

				$(this).append(template);
			});

			// add "effect" class to the grid
			$grid.addClass('tt-effect-active');
			
			// wait that animations end
			var onEndAnimFn = function() {
				// remove old elements
				$('.tt-old').each(function(i, el){ 
					el.remove();

					// remove class "tt-empty" from the empty items
					$(el).removeClass( 'tt-empty' );
					
					// now apply that same class to the items that got no children (special case)
					if ( !el.hasChildNodes() ) {
						$(el).addClass( 'tt-empty' );
					}
				});
				
				// remove the "effect" class
				$grid.removeClass('tt-effect-active');
				isAnimating = false;

				// $photos.magicWall("start");
			};

			if( support ) {
				onAnimationEnd( items, items.size(), onEndAnimFn );
			} else {
				onEndAnimFn.call();
			}

		}, 25 );
		
	}

	function onAnimationEnd( elems, len, callback ) {
		var finished = 0,
			onEndFn = function() {
				// this.removeEventListener( animEndEventName, onEndFn );
				++finished;
				if( finished === len ) {
					callback.call();
				}
			};

		elems.each(function(index){
			$(this).find('.magicwall-wrap').on(animEndEventName, onEndFn);
		});
	}

